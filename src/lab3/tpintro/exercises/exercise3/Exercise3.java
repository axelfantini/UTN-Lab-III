package lab3.tpintro.exercises.exercise3;

public class Exercise3 {
    public static void main(String[] args)
    {
        int N = 5;
        System.out.println("N: " + N);
        N += 77;
        System.out.println("N + 77 = " + N);
        N = 3;
        System.out.println("N = 3");
        N *= 2;
        System.out.println("N * 2 = " + N);
    }
}
